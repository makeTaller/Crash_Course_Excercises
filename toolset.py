garage_tools = ["Air Rachet", "Impact Wrench", "Hammer", "Socket Set"]

print("Original list")
print(garage_tools)



print("Sorted list")
garage_tools.sort()
print(garage_tools)



print("Reverse list")
garage_tools.reverse()
print(garage_tools)



print("Re-Reversed list")
garage_tools.reverse()
print(garage_tools)



print("Reverse Sorted list")
garage_tools.sort(reverse=True)
print(garage_tools)


